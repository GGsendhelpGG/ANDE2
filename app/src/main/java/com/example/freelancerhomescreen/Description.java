package com.example.freelancerhomescreen;

public class Description {

    boolean visibility;
    String name;
    String skills;
    String description;


    public Description(boolean visibility) {
        this.visibility = visibility;
        this.name = name;
        this.skills = skills;
        this.description = description;
    }

    public void setVisibility(boolean visibility){
        this.visibility = visibility;
    }

}
